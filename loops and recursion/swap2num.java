public class swap2num {
    public static void main(String args[]) {
        int a, b, temp;

        a = 15;
        b = 17;
        System.out.println("Before swapping:" + a + " " + b);
        temp = a;
        a = b;
        b = temp;
        System.out.println("after swapping:" + a + " " + b);

    }
}
